function chunkArrayInGroups(arr, size) {
  // Break it up.
  var newArray = [];
  var a = arr.slice(0,size);
  var b = arr.slice(size, (size+size));

  newArray.push(a);
  newArray.push(b);
  return newArray;  
}

chunkArrayInGroups([0, 1, 2, 3, 4, 5], 3);



